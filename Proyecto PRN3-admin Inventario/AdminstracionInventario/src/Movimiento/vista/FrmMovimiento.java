    private void tblMovimientosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMovimientosMouseClicked
        int fila = tblMovimientos.getSelectedRow();
    if (fila >= 0) {
        movimientoSeleccionadoId = Integer.parseInt(tblMovimientos.getValueAt(fila, 0).toString());
        cbTipo.setSelectedItem(tblMovimientos.getValueAt(fila, 1).toString());
        txtMarca.setText(tblMovimientos.getValueAt(fila, 3).toString());
        txtModelo.setText(tblMovimientos.getValueAt(fila, 4).toString());
        txtCantidad.setText(tblMovimientos.getValueAt(fila, 5).toString());
        txtCosto.setText(tblMovimientos.getValueAt(fila, 6).toString());
        txtFecha.setText(tblMovimientos.getValueAt(fila, 7).toString());

        // Selecciona el producto correcto en el combo
        String nombreProducto = tblMovimientos.getValueAt(fila, 2).toString();
        for (int i = 0; i < cbProducto.getItemCount(); i++) {
            Producto prod = cbProducto.getItemAt(i);
            if (prod.getNombre().equals(nombreProducto)) {
                cbProducto.setSelectedIndex(i);
                break;
            }
        }

        // Selecciona el proveedor correcto en el combo
        String nombreProveedor = tblMovimientos.getValueAt(fila, 8).toString();
        for (int i = 0; i < cbProveedor.getItemCount(); i++) {
            Proveedor prov = cbProveedor.getItemAt(i);
            if (prov.getNombre().equals(nombreProveedor)) {
                cbProveedor.setSelectedIndex(i);
                break;
            }
        }
    }
    }//GEN-LAST:event_tblMovimientosMouseClicked

