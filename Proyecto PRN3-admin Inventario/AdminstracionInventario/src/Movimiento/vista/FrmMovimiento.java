    private void cargarProductos(){
        cbProducto.removeAllItems();
        List<Producto> productos = productoDAO.listarProductos();
        for (Producto p : productos){
            cbProducto.addItem(p);
        }
    }
    
    private void cargarProveedores(){
        List<Proveedor> proveedores = proveedorDAO.listarProveedores();
        for (Proveedor p : proveedores){
            cbProveedor.addItem(p);
        }
    }
    
    private void llenarCamposProducto() {
        Producto seleccionado = (Producto) cbProducto.getSelectedItem();
        if (seleccionado != null) {
            txtMarca.setText(seleccionado.getMarca());
            txtModelo.setText(seleccionado.getModelo());
        }
    }
    
    private void cargarTablaMovimientos() {
        String[] columnas = {"ID", "Tipo", "Producto", "Marca", "Modelo", "Cantidad", "Costo Unitario", "Fecha", "Proveedor"};
        DefaultTableModel modeloTabla = new DefaultTableModel(null, columnas);
        List<Movimiento> lista = movimientoDAO.listarMovimientos();

        for (Movimiento m : lista) {
            modeloTabla.addRow(new Object[]{
                    m.getId(),
                    m.getTipo(),
                    m.getId_producto(),
                    m.getMarca(),
                    m.getModelo(),
                    m.getCantidad(),
                    m.getCosto_unitario(),
                    m.getFecha(),
                    m.getId_proveedor()
            });
        }
        tblMovimientos.setModel(modeloTabla);
    }
    
    private void limpiarCampos() {
        cbTipo.setSelectedIndex(0);
        cbProducto.setSelectedIndex(0);
        txtMarca.setText("");
        txtModelo.setText("");
        txtCantidad.setText("");
        txtCosto.setText("");
        txtFecha.setText("");
        movimientoSeleccionadoId = -1;
    }
    
    

