    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        if (movimientoSeleccionadoId == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona un movimiento de la tabla para actualizar.");
            return;
        }

        try {
            Producto p = (Producto) cbProducto.getSelectedItem();
            String tipo = cbTipo.getSelectedItem().toString();
            int cantidad = Integer.parseInt(txtCantidad.getText());
            double costo = Double.parseDouble(txtCosto.getText());
            String fecha = txtFecha.getText();
            Proveedor prov = (Proveedor) cbProveedor.getSelectedItem();

            Movimiento m = new Movimiento(tipo, p.getId_producto(), p.getMarca(), p.getModelo(), cantidad, costo, fecha, prov.getId_proveedor());
            m.setId(movimientoSeleccionadoId);

            if (movimientoDAO.actualizarMovimiento(m)) {
                JOptionPane.showMessageDialog(this, "Movimiento actualizado correctamente");
                cargarTablaMovimientos();
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Error al actualizar el movimiento");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            Producto p = (Producto) cbProducto.getSelectedItem();
            String tipo = cbTipo.getSelectedItem().toString();
            int cantidad = Integer.parseInt(txtCantidad.getText());
            double costo = Double.parseDouble(txtCosto.getText());
            String fecha = txtFecha.getText();
            Proveedor prov = (Proveedor) cbProveedor.getSelectedItem();

            Movimiento m = new Movimiento(tipo, p.getId_producto(), p.getMarca(), p.getModelo(), cantidad, costo, fecha, prov.getId_proveedor());

            if (movimientoDAO.registrarMovimiento(m)) {
                JOptionPane.showMessageDialog(this, "Movimiento guardado correctamente");
                cargarTablaMovimientos();
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Error al guardar el movimiento");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

